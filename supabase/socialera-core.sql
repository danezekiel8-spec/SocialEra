create or replace function public.set_socialera_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = timezone('utc', now());
  return new;
end;
$$;

create or replace function public.sync_social_post_reactions(
  p_post_id uuid,
  p_likes_count integer default 0,
  p_like_actor_ids jsonb default '[]'::jsonb,
  p_like_actors jsonb default '[]'::jsonb,
  p_saves_count integer default 0,
  p_save_actor_ids jsonb default '[]'::jsonb
)
returns public.social_posts
language plpgsql
security definer
set search_path = public
as $$
declare
  updated_row public.social_posts;
begin
  if auth.uid() is null then
    raise exception 'Authentication required';
  end if;

  update public.social_posts
  set
    likes_count = greatest(coalesce(p_likes_count, 0), 0),
    like_actor_ids = case when jsonb_typeof(coalesce(p_like_actor_ids, '[]'::jsonb)) = 'array' then coalesce(p_like_actor_ids, '[]'::jsonb) else '[]'::jsonb end,
    like_actors = case when jsonb_typeof(coalesce(p_like_actors, '[]'::jsonb)) = 'array' then coalesce(p_like_actors, '[]'::jsonb) else '[]'::jsonb end,
    saves_count = greatest(coalesce(p_saves_count, 0), 0),
    save_actor_ids = case when jsonb_typeof(coalesce(p_save_actor_ids, '[]'::jsonb)) = 'array' then coalesce(p_save_actor_ids, '[]'::jsonb) else '[]'::jsonb end
  where id = p_post_id
  returning * into updated_row;

  if updated_row.id is null then
    raise exception 'Social post not found';
  end if;

  return updated_row;
end;
$$;

create or replace function public.sync_social_comment_reactions(
