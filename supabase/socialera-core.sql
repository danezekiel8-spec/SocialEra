  media_url text not null default '',
  caption_title text not null default '',
  caption_text text not null default '',
  tags jsonb not null default '[]'::jsonb,
  match_title text not null default 'Seen in this post',
  promote_enabled boolean not null default false,
  promoted_title text not null default '',
  promoted_price text not null default '',
  promoted_text text not null default '',
  likes_count integer not null default 0,
  comments_count integer not null default 0,
  saves_count integer not null default 0,
  like_actor_ids jsonb not null default '[]'::jsonb,
  like_actors jsonb not null default '[]'::jsonb,
  save_actor_ids jsonb not null default '[]'::jsonb,
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
