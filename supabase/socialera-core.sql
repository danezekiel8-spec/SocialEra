create table if not exists public.store_products (
  id bigint generated by default as identity primary key,
  name text not null,
  price numeric(12, 2) not null default 0,
  category text not null default '',
  image text not null default '',
  stock integer not null default 0,
  featured boolean not null default false,
  description text not null default '',
  fulfillment_type text not null default 'inhouse' check (fulfillment_type in ('inhouse', 'dropship')),
  supplier_name text not null default '',
  supplier_sku text not null default '',
  supplier_cost numeric(12, 2) not null default 0,
  supplier_link text not null default '',
  processing_time text not null default '',
  shipping_time text not null default '',
  sale_enabled boolean not null default false,
  sale_price numeric(12, 2) not null default 0,
  sale_label text not null default 'Sale',
  created_at timestamptz not null default timezone('utc', now()),
  updated_at timestamptz not null default timezone('utc', now())
);

create table if not exists public.social_posts (
  id uuid primary key default gen_random_uuid(),
